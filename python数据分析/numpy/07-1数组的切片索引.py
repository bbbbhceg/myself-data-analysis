import numpy

list = [
  [80,89,86,67,79],[78,97,89,67,81],
  [90,94,78,67,74],[91,91,90,67,69],
  [76,87,75,67,86],[70,79,84,67,84],
  [94,92,93,67,64],[86,85,83,67,80]
]
list = numpy.array(list)

list_2 = list[2,:3]    # 对第三个列表的前三个元素进行切片，赋予list_2
list_3 = list[1][:3]   # 对第二个列表的前三个元素进行切片。赋予list_3
# list[2,:3] = 99999999  # 多元素批量修改      #####我在此发现切片索引，好像真的是索引并没有真的copy数值，在切片之后变动切片出，最后打印切片也会随着变化
print(list_2)   # [90 94 78]
print(list_3)   # [78 97 89]


